    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                
        header.clear();
        data.clear();
        
        try
        {
                
        Connection con=DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=city_gym;username=sa;password=aptech");
        Statement s=con.createStatement();
        
        String query="select * from member";
        ResultSet res=s.executeQuery(query);
        while(res.next())
        {
        String m_id=res.getString(1);
        String fname=res.getString(2);
        String lname=res.getString(3);
        String c_no=res.getString(4);
        String adres=res.getString(5);
        String inst_id=res.getString(6);
        String doj=res.getString(7);
        String plan_type=res.getString(8);
        String amount=res.getString(9);
            
        Vector<String> row=new Vector<String>();
        
        row.add(m_id);
        row.add(fname);
        row.add(lname);
        row.add(c_no);
        row.add(adres);
        row.add(inst_id);
        row.add(doj);
        row.add(plan_type);
        row.add(amount);

        data.add(row);
        
        }
      header.add("Member Id"); 
      header.add("First Name");
      header.add("Last Name");
      header.add("Contact No"); 
      header.add("Address");
      header.add("Instructor ID");
      header.add("Date Of Joining");
      header.add("Plan Type"); 
      header.add("Amount");
      memtable.setModel(new DefaultTableModel(data,header));
       
        
        
        
        }
        
            catch(Exception e)
            
            {
                System.out.println(e);
            }

    }//GEN-LAST:event_jButton1ActionPerformed

