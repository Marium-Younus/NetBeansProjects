use Management
create table Nurse
(
Id int not null,
F_name varchar(20),
L_name varchar(20),
Join_Date varchar(20),
Age varchar(20),
Gender varchar(20),
P_Address varchar(100),
Email varchar(20),
Contact varchar(20),
Desination varchar(20),
Timing varchar(20),

constraint pk3 primary key (Id)
)
select * from Nurse