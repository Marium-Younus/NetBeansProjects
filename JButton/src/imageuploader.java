    private void uploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadActionPerformed

        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File("home.user")); 
        FileNameExtensionFilter filter = new FileNameExtensionFilter("All images", "gif", "png", "jpg");
        file.setAcceptAllFileFilterUsed(false); 
        file.addChoosableFileFilter(filter);
        
        int result = file.showOpenDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            filepath.setText(path); 
            image.setIcon(ResizeImage(path)); 

//        ImageIcon ic = new ImageIcon(selectedFile.getAbsolutePath());
//        image.setIcon(ic);
        }
        else if(result == JFileChooser.CANCEL_OPTION){
            image.setIcon(null); 
            filepath.setText(null); 
            image.setText("No Image Selected"); 
        }     
    }//GEN-LAST:event_uploadActionPerformed

