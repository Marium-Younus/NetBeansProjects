    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     File f1;
        if(!jTextField1.getText().isEmpty())
        {
           f1 = new File(jTextField1.getText());
        }
        else
        {
         f1= new File(jComboBox1.getSelectedItem().toString());
        }
        
        try
        {
            FileInputStream fis=new FileInputStream(f1);
            BufferedInputStream bis= new BufferedInputStream(fis);
            DataInputStream dis= new DataInputStream(bis);
            while(dis.available()>0)
            {
            jTextArea1.setText(dis.readLine());
            
            }
            
           
        
        }catch(Exception ex)
        {
            System.out.println(""+ex);
        
        
        } 
        if(success==true)
        {
             JOptionPane.showMessageDialog(rootPane,"File read");
            
        }
        
        else
        {
        
        JOptionPane.showMessageDialog(rootPane,"File not read");
        }
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

